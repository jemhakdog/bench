import sqlite3
from indiviualitemscrapper import ProductExtractor
import json
from concurrent.futures import ThreadPoolExecutor

connection = sqlite3.connect('ecommerce.db')
cursor = connection.cursor()

# Function to process each item
def process_item(item):
    link = item['link']
    id = item['product_id']
    extractor = ProductExtractor(link)
    data_extracted = json.dumps(extractor.extract_data()).replace("'", "''")

    try:
        sql = f'''
            INSERT OR IGNORE INTO product (product_id, data)
            VALUES (
                '{id}',
                '{data_extracted}'
            );
        '''
        cursor.execute(sql)
        connection.commit()  # Commit the transaction
    except Exception as e:
        print(f"Error processing item {id}: {e}")

# Number of workers (threads)
num_workers = 10000

# Using ThreadPoolExecutor to process items concurrently
with ThreadPoolExecutor(max_workers=num_workers) as executor:
    futures = [executor.submit(process_item, item) for item in data]

# Close the database connection
connection.close()